export const getFeatured = () => projects.filter((p) => p.featured)

export const getByCategory = (cat) =>
  cat === 'All' ? projects : projects.filter((p) => p.category === cat)

export const getBySlug = (slug) => projects.find((p) => p.slug === slug)

export const getAdjacentProjects = (slug) => {
  const idx = projects.findIndex((p) => p.slug === slug)
  return {
    prev: idx > 0 ? projects[idx - 1] : projects[projects.length - 1],
    next: idx < projects.length - 1 ? projects[idx + 1] : projects[0],
  }
}
